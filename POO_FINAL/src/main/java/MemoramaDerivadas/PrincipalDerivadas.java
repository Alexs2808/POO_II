        public void startTimer() {

    if (gameTimer != null) {
        gameTimer.stop();
    }

    seconds = 60;  // INICIA EN 60 SEGUNDOS (1 MINUTO)
    gameRunning = true;

    lblTimer.setText("01:00");

    gameTimer = new javax.swing.Timer(1000, (e) -> {

        seconds--;

        // ---- CUANDO LLEGA A CERO ----
        if (seconds <= 0) {
            lblTimer.setText("00:00");
            stopTimer();
            JOptionPane.showMessageDialog(null, "Â¡Tiempo agotado!");
            return;
        }

        int min = seconds / 60;
        int sec = seconds % 60;
        lblTimer.setText(String.format("%02d:%02d", min, sec));
    });

    gameTimer.start();
}

        
        public static boolean gameRunning = false;
        public static Timer gameTimer = null;

        public static void stopTimer() {
            gameRunning = false;
            if (gameTimer != null) {
                gameTimer.stop();
            }
        }

        
